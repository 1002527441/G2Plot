function getWordCloudConfig() {
  return {
    width: 600,
    height: 400,
    data: data,
    maskImage: 'https://gw.alipayobjects.com/mdn/rms_2274c3/afts/img/A*07tdTIOmvlYAAAAAAAAAAABkARQnAQ',
    color: (word, weight) => {
      return getRandomColor();
    },
    shape: 'cardioid',
    ellipticity: 1,

    minRotation: -Math.PI / 2,
    maxRotation: Math.PI / 2,
    rotateRatio: 0.5,
    rotationSteps: 4,

    gridSize: 8,
    shuffle: false,
    backgroundColor: '#fff',
    wait: 0,

    enableToolTips: true,
    enableEmphasis: true,
    hoveredId: -1,
    shadowColor: '#333333',
    shadowBlur: 10,

    hover: hoverAction,
  };
}

